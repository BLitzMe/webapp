export function setPostDate(date) {
    return {
        type: "SET_POST_DATE",
        payload: date
    }
}

export function setName(name) {
    return {
        type: "SET_NAME",
        payload: name
    }
}

export function setOrt(ort) {
    return {
        type: "SET_ORT",
        payload: ort
    }
}
export function setDescription(description) {
    return {
        type: "SET_DESCRIPTION",
        payload: description
    }
}
export function setTitle(title) {
    return {
        type: "SET_TITLE",
        payload: title
    }
}